"""
BetVector — Match Analysis Narrative Generator (E18-01)
=======================================================
Synthesises model predictions, team features, head-to-head records,
and contextual data into a structured, human-readable match analysis.

This is a **pure Python module** — no Streamlit, no database queries.
It takes the same ``data`` dict that ``match_detail.load_match_data()``
returns and produces a :class:`MatchNarrative` dataclass that any
consumer (dashboard page, email template, API endpoint) can render.

The narrative answers the user's core question:
"Why does the model predict what it predicts?"

Architecture
------------
1. Eight *factor generators* each assess one signal (form, xG, venue,
   H2H, squad value, pressing style, weather, rest days).
2. Each returns an optional :class:`NarrativeFactor` with a significance
   score (0.0–1.0).  Weak signals are filtered; strong ones rise to the
   top.  Only the top 6 factors are shown to prevent information overload.
3. A headline generator summarises the overall prediction strength.
4. A value summary explains whether value bets exist and why.
5. For finished matches, a result comparison shows model accuracy.

Master Plan refs: MP §3 Flow 1 Step 8, MP §8 Design System
"""

from __future__ import annotations

import math
from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional, Tuple


# ============================================================================
# Dataclasses
# ============================================================================

@dataclass
class NarrativeFactor:
    """A single explanatory factor in the match narrative.

    Each factor represents one signal the model uses — form, xG quality,
    venue advantage, etc.  Factors are ranked by *significance* so the UI
    shows only the most meaningful ones.

    Attributes
    ----------
    category : str
        Internal key: "form", "xg", "venue", "h2h", "squad", "pressing",
        "weather", "rest".
    headline : str
        One-line summary, e.g. "Arsenal in strong form".
    detail : str
        Supporting data, e.g. "2.40 PPG over last 5 vs Chelsea's 1.20".
    direction : str
        "positive" (supports prediction), "negative" (works against it),
        or "neutral" (contextual information).
    significance : float
        0.0–1.0, used for ranking and filtering.
    """
    category: str
    headline: str
    detail: str
    direction: str
    significance: float


@dataclass
class NarrativeValueSummary:
    """Summary of value betting opportunities for this match."""
    has_value_bets: bool
    count: int
    best_market: Optional[str] = None
    best_edge: Optional[float] = None
    summary_text: str = ""


@dataclass
class NarrativeResult:
    """Result comparison for a finished match."""
    actual_home_goals: int
    actual_away_goals: int
    predicted_correct_1x2: bool
    predicted_correct_ou25: bool
    result_text: str


@dataclass
class MatchNarrative:
    """Complete narrative for a match, ready for rendering.

    Generated by :func:`generate_match_narrative`.  Contains all pieces
    the UI needs: headline, expected goals, ranked factors, value summary,
    and (for finished matches) a result comparison.
    """
    headline: str
    headline_colour: str           # "green", "yellow", or "text"
    predicted_home_goals: float    # lambda_home from Poisson model
    predicted_away_goals: float    # lambda_away from Poisson model
    home_team: str
    away_team: str
    is_finished: bool
    factors: List[NarrativeFactor] = field(default_factory=list)
    value_summary: Optional[NarrativeValueSummary] = None
    result: Optional[NarrativeResult] = None
    confidence_label: str = "close"  # "strong", "moderate", "close"


# ============================================================================
# Selection / Market label mapping (mirrors picks.py)
# ============================================================================

_SELECTION_LABELS = {
    ("1X2", "home"): "Home Win",
    ("1X2", "draw"): "Draw",
    ("1X2", "away"): "Away Win",
    ("OU25", "over"): "Over 2.5 Goals",
    ("OU25", "under"): "Under 2.5 Goals",
    ("OU15", "over"): "Over 1.5 Goals",
    ("OU15", "under"): "Under 1.5 Goals",
    ("OU35", "over"): "Over 3.5 Goals",
    ("OU35", "under"): "Under 3.5 Goals",
    ("BTTS", "yes"): "Both Teams to Score",
    ("BTTS", "no"): "Both Teams NOT to Score",
}


# ============================================================================
# Helper — safe attribute access
# ============================================================================

def _ga(obj: Any, attr: str) -> Optional[float]:
    """Safe getattr that returns None for missing or non-numeric values."""
    if obj is None:
        return None
    val = getattr(obj, attr, None)
    if val is None:
        return None
    try:
        return float(val)
    except (ValueError, TypeError):
        return None


