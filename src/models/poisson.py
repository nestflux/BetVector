"""
BetVector — Poisson Regression Model (E5-02)
==============================================
BetVector's foundational prediction model.  Uses Poisson regression to
estimate the expected number of goals each team will score, then builds
a 7×7 scoreline probability matrix from those estimates.

How the Poisson Model Works
---------------------------
The **Poisson distribution** models the probability of a given number of
events (goals) occurring in a fixed interval (a match), given a known
average rate (lambda).  It's the standard starting point for football
prediction because:

  1. Goals are relatively rare events (typically 1-3 per team per match)
  2. They occur roughly independently of each other
  3. The rate of scoring depends on team quality and matchup

The model estimates two lambda values per match:
  - **lambda_home** (expected home goals) = f(home_attack, away_defence, home_advantage)
  - **lambda_away** (expected away goals) = f(away_attack, home_defence)

Attack Strength and Defence Strength
-------------------------------------
Each team has an **attack strength** (how many goals they tend to score)
and a **defence strength** (how many goals they tend to concede).  These
are estimated from rolling features like goals_scored_5, goals_conceded_5,
xg_5, etc.

The **home advantage** effect is real and significant in football — home
teams score roughly 0.3 more goals per match on average.  The model
learns this from the data.

Building the Scoreline Matrix
-----------------------------
Once we have lambda_home and lambda_away, we compute the probability of
every scoreline from 0-0 to 6-6:

    P(home=h, away=a) = poisson.pmf(h, lambda_home) × poisson.pmf(a, lambda_away)

This assumes **independence** between home and away goals — a simplifying
assumption that works well in practice for match-level predictions.
The matrix is truncated at 6 goals and renormalised so all 49 cells sum
to exactly 1.0.

Why Independence Works (Mostly)
-------------------------------
The independence assumption means we treat "Arsenal scoring 2" and
"Chelsea scoring 1" as independent events.  In reality there's a weak
correlation (e.g., a team chasing a deficit may score more but also
concede more).  However, this correlation is small enough that the
independent Poisson model remains competitive with more complex models.

Dixon-Coles Correction (PC-21)
-------------------------------
Dixon & Coles (1997) fix the known weakness of independent Poisson in
low-scoring matches.  A single parameter ρ (rho) adjusts the (0,0),
(1,0), (0,1), and (1,1) cells of the scoreline matrix.  ρ is estimated
per-league via maximum likelihood from training data (temporal integrity
preserved).  When ρ = 0.0, the correction is a no-op and produces
identical output to standard independent Poisson.

Master Plan refs: MP §4 Prediction Models, MP §5 Scoreline Matrix, MP §7 Model Interface
"""

